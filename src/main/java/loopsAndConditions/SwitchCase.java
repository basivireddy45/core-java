package loopsAndConditions;

public class SwitchCase {
    public static void main(String[] args) {


        int a = 1;
        switch (a) {

            case 1:
                System.out.println("kalakoti");
                break;
            case 2:
                System.out.println("Basivi");
                break;
            default:
                System.out.println("Reddy");
        }
    }
}
